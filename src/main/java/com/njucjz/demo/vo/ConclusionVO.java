package com.njucjz.demo.vo;

public class ConclusionVO {
    private int livingTime;
    private int activeUserAmount;
    private int letterCount;

    public int getLivingTime() {
        return livingTime;
    }

    public void setLivingTime(int livingTime) {
        this.livingTime = livingTime;
    }

    public int getActiveUserAmount() {
        return activeUserAmount;
    }

    public void setActiveUserAmount(int activeUserAmount) {
        this.activeUserAmount = activeUserAmount;
    }

    public int getLetterCount() {
        return letterCount;
    }

    public void setLetterCount(int letterCount) {
        this.letterCount = letterCount;
    }
}
